# This is a simple example of defining and using variables in Python

# Defining variables
name = "Alice"              # string variable
age = 25                    # integer variable
height = 5.6                # float variable
is_student = True           # boolean variable

# Printing variable values
print("Name:", name)
print("Age:", age)
print("Height:", height)
print("Is a student?", is_student)

# Doing some operations
next_year_age = age + 1
print("Next year, age will be:", next_year_age)

# Combining strings
greeting = "Hello, my name is " + name
print(greeting)
